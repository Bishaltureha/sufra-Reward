// Utility functions for phone number handling
export function calculatePhoneMaxLength(country) {
  if (!country || !country.phoneLength) {
    return 20; // Default fallback
  }

  const format = phoneFormats[country.code] || phoneFormats.DEFAULT;
  return country.phoneLength + format.spacingChars;
}

export function getPhoneMaxLength(selectedCountry) {
  if (!selectedCountry) return 20;
  return selectedCountry.maxLength || calculatePhoneMaxLength(selectedCountry);
}

export function formatPhoneNumber(country, phoneNumber) {
  if (!country || !phoneNumber) return phoneNumber;

  const cleanNumber = phoneNumber.replace(/\D/g, "");
  const format = country.phoneFormat || phoneFormats.DEFAULT;

  // Basic formatting - this can be enhanced based on specific patterns
  return cleanNumber.replace(/(\d{3})(\d{3})(\d{4})/, "($1) $2-$3");
}

// Updated helper functions
export const getCountryByCode = (code) => {
  return countries.find((country) => country.code === code);
};

export const getCountryByDialCode = (dialCode) => {
  return countries.find((country) => country.dial_code === dialCode);
};

export const getCountriesByName = (searchTerm) => {
  return countries.filter(
    (country) =>
      country.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
      country.nativeName.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

export const getCountriesByLanguage = (languageCode) => {
  return countries.filter((country) => country.languageCode === languageCode);
};

export const validatePhoneNumber = (countryCode, phoneNumber) => {
  const country = getCountryByCode(countryCode);
  if (!country) return false;

  // Remove any non-digit characters
  const cleanNumber = phoneNumber.replace(/\D/g, "");

  // Check if the length matches expected phone length
  return cleanNumber.length === country.phoneLength;
};

export const getCountryDisplayName = (countryCode, useNativeName = false) => {
  const country = getCountryByCode(countryCode);
  if (!country) return null;

  return useNativeName ? country.nativeName : country.name;
};

// New function to get input max length for a specific country
export const getInputMaxLength = (selectedCountry) => {
  if (!selectedCountry) return 20;
  return getPhoneMaxLength(selectedCountry);
};
